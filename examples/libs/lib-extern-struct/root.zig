const std = @import("std");
//--------------------------------------------------------------------------------
pub const ExampleEnum = enum(u8) { no = 0, yes = 1 };
pub const ExampleRawString = extern struct { ptr: [*]const u8, len: usize };
pub const Self = extern struct {
    example_byte: u8 = 0,
    example_enum: ExampleEnum = .no,
    example_raw_string: ExampleRawString = .{ .ptr = "", .len = 0 },
};
//------------------------------------------------------------
export fn init() callconv(.c) Self {
    //------------------------------------------------------------
    const self = Self{};
    //------------------------------------------------------------
    return self;
    //------------------------------------------------------------
}
//--------------------------------------------------------------------------------
export fn setByte(self: *Self, value: u8) callconv(.c) bool {
    //------------------------------------------------------------
    self.example_byte = value;
    //------------------------------------------------------------
    return true;
    //------------------------------------------------------------
}
//--------------------------------------------------------------------------------
export fn getByte(self: *Self) callconv(.c) u8 {
    //------------------------------------------------------------
    return self.example_byte;
    //------------------------------------------------------------
}
//--------------------------------------------------------------------------------
export fn setEnum(self: *Self, enum_value: ExampleEnum) callconv(.c) bool {
    //------------------------------------------------------------
    self.example_enum = enum_value;
    //------------------------------------------------------------
    return true;
    //------------------------------------------------------------
}
//--------------------------------------------------------------------------------
export fn getEnum(self: *Self) callconv(.c) ExampleEnum {
    //------------------------------------------------------------
    return self.example_enum;
    //------------------------------------------------------------
}
//--------------------------------------------------------------------------------
export fn setRawString(self: *Self, raw_string_value: ExampleRawString) callconv(.c) bool {
    //------------------------------------------------------------
    self.example_raw_string = raw_string_value;
    //------------------------------------------------------------
    return true;
    //------------------------------------------------------------
}
//--------------------------------------------------------------------------------
export fn getRawString(self: *Self) callconv(.c) ExampleRawString {
    //------------------------------------------------------------
    return self.example_raw_string;
    //------------------------------------------------------------
}
//--------------------------------------------------------------------------------
